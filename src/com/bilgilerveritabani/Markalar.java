package com.bilgilerveritabani;



public class Markalar {
	private  String markalar;
	private  int markaSayilari;
	public Markalar(String markalar, int markaSayilari) {
		
		this.markalar = markalar;
		this.markaSayilari =markaSayilari;
	}
	
	
	
	public String getMarkalar() {
		return markalar;
	}
	public int getMarkaSayilari() {
		return markaSayilari;
	}
	
	
	

}
